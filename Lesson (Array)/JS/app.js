// Array Set Up
let fruits = ['apple', 'strawberry', 'cherry', 'orange'];
console.log(fruits);

// Selecting specific item within array
console.log(fruits[0]);

// Variables as index numbers
let three = 3;
console.log(fruits[three]);

// Updating Array
fruits[1] = 'blueberry';
console.log(fruits);

// Adding items to the end of an array
fruits.push('strawberry');
console.log(fruits);

// Removing item at the end of an array
fruits.pop();
console.log(fruits);

// Finding total number of items in array
console.log(fruits.length);